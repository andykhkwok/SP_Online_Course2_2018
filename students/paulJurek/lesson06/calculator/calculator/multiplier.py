"""defines mulitplying behavior"""


class Multiplier:
    """basic mulitplier object"""

    @staticmethod
    def calc(operand_1, operand_2):
        """multiplies input operands
        args:
            operand_1: numeric
            operand_2: numeric
        returns:
            product of input operands
        """
        return operand_1*operand_2
