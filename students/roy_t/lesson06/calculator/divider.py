"""Divider module"""


class Divider:
    """Class Divider"""
    @staticmethod
    def calc(operand_1, operand_2):
        """
        Calculate the quotient of two operands.
        :param operand_1: The numerator.
        :param operand_2: The denominator.
        :return: quotient of operand 1 and 2
        """
        return operand_1/operand_2
