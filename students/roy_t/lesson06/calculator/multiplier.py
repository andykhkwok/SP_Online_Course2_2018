"""Multiplier module"""


class Multiplier:
    """Class for multiplying to operands."""
    @staticmethod
    def calc(operand_1, operand_2):
        """
        Calculate the product of two operands.
        :param operand_1: The first operands in the stack.
        :param operand_2: The second operand in the stack.
        :return: product of two operands
        """
        return operand_1*operand_2
