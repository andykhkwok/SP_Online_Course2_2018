"""Module for Adder"""


class Adder:
    """Adder class provides result of combining two operands"""

    @staticmethod
    def calc(operand_1, operand_2):
        """
        Calculate the value of two operands
        :param operand_1: value of first operand
        :param operand_2: value of second operand
        :return: sum of operands
        """
        return operand_1 + operand_2
